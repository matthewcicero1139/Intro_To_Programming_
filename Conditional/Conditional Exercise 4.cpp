#include <iostream>
int main()
{
	int k = 1;
	switch (k)
	{
	case 0:
		//std::cout << "1";
		printf("1");
		system("pause");
		break;
	case 1:
		//std::cout << "2 or 3";
		printf("2 or 3");
		system("pause");
		break;
	case 2:
		//std::cout << "4";
		printf("4");
		system("pause");
		break;
	default:
		//std::cout << "Invalid";
		printf("invalid");
		system("pause");
		break;
	}
}